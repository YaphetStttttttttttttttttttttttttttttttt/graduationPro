package com.gp.model.vo;

public class Sex {
	private int num;
	private String sex;
	public Sex() {}
	public Sex(int num, String sex) {
		this.num = num;
		this.sex = sex;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	
}
